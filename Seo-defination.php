											<h3>SEA: Search Engine Advertising</h3>
											<p>This is the advertising on search engines. This is the time to optimize the results of sponsored links (that is to say paid links). It is a shopping search engine optimization, Google Adwords type.</p>


																				
											<h3>SMO: Social Media Optimization</h3>
											<p>It is the optimization for social media. This optimization strategy is to disseminate information and to acquire traffic through social networks. It's a similar trend in e-marketing, to generate potential indirect links, and thereby increase the popularity of a site.</p>
											
											<h3>SEM: Search Engine Marketing</h3>
											<p>It's the marketing for search engines. This term includes all SEO and paid techniques to optimize the visibility of a website on search engines: SEM = SEO + SEA + GOS.</p>
											
											<h3>SERP: Search Engine Result Pages</h3>
											<p>Results pages of a search engine
This is a web page that a search engine like Google delivers compared to the keywords that the user enters. It presents a list of links to the websites of pages or resources that the search engine refers to the most relevant vis this motion screw.</p>
											
											<h3>META tags</h3>
											<p>Meta tags are used to describe the meta data of an HTML page but is not displayed on this page. This information is used web browsers, search engines, all indexing tools. Content-Type, Description, Charset. Some are used for SEO, such as meta description and robots.</p>
											
											<h3>Keywords</h3>
											<p>This is a meta tag describing the keywords but has no little importance today.Crawlers no longer use it for positioning. It is therefore more useful to use it in this context.</p>
											
											<h3>Title</h3>
											<p>The title tag defines the title of a web page . In the search results of Google, it is used as a blue link, before the site description. This is not a meta tag, but a simple structure tag of an HTML document. This area is called strategic SEO as favored by search engines for locating a page. Not to be confused, the title attribute of an image or a link is not used by the algorithm crawler. Its usefulness is simply to display a tooltip on mouse over the link.</p>
											
											<h3>Anchor text</h3>
											<p>Anchor text of a link anchor is the text (or image, button, ...) on which is placed a link to another web page or another section of the same page. This text is also to optimize as securities and other strategic areas SEO. However careful not to overdo it because Google Penguin monitors it closely. could otherwise penalize you ... Strategic keywords will therefore be placed in the titles of links but avoiding to always place the same. 
For an image forming anchor office, the robot scans the alt attribute, which means alternative text and the context of the link. Be aware that the algorithm given less weight in the alt attribute as a real text.</p>
											
											<h3>Snippet</h3>
											<p>Extract essential element of a results page of a search engine, the snippet corresponding to a title and two lines of description. Generally, these two elements are the title tag and meta description of the page. Sometimes the crawler decides otherwise.Indeed, the meta description may not be informed or may be deemed inappropriate to the content of the scanned page. In this case, the robot selects a most relevant textual content present in this page.</p>
											
											<h3>Duplicate content</h3>
											<p>Duplicate Content Search Engine has no interest to show multiple identical results for the same query. He prefers course show different results to best meet the user making the request. The engine will then choose one of the results to view it in its SERPs: that he considers to be the original content. Duplicate content will be penalized. That's why everyone should avoid producing duplicate content, either by copying content from another site, or by providing redundant content on your own pages.</p>
											
											<h3>Backlink</h3>
											<p>Inbound link the more links pointing to your pages and your SEO is effective. The popularity of your website is a very important criterion for the search engines. Your strategy of acquiring links, net linking said, should focus on quality links rather than a large quantity of low-quality links.</p>
											
											
											
											
